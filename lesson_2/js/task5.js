// Задача 5. Дано кількість секунд, що пройшла від початку доби. Визначити скільки це годин і хвилин.
// вхідні дані
let seconds = parseInt(prompt('введи кількість секунд', ''))
// обчислення
let minuts = seconds / 60 
let hours = minuts / 60
// виводимо
alert(`твої ${seconds} секунд, це ${minuts} хвилин, та ${hours} годин. З округленням до нормальних цифр, це ${Math.round(minuts)} хвилин та ${Math.round(hours)} годин`)
