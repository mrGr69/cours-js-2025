// Задача 4. Дано довжину у сантиметрах. Визначати скільки це метрів і кілометрів.
// вхідні дані
let LenghtSM = parseInt(prompt('введи, скільки маєш см великого пальця', ''))
// обчислення
let meters = LenghtSM / 100
let kilometers = LenghtSM / 1000
// Вивидення
alert(`Довжина твого пальця буде ${meters} метрів. Але для баби Галі ми округлимо до ${Math.ceil(meters)} метрів \nА також це буде ${kilometers} кілометра`)
